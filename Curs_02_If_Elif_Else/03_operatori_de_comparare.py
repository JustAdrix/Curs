# OPERATORI DE COMPARARE

# operatori de comparare exacta : "=="
nume = "Creanga"
alt_nume = "creanga"

print(nume == alt_nume)

alt_nume = alt_nume.capitalize()
print(nume == alt_nume)

print("-"*40)

# operatori de difirentiere: "!="
a, b = 10, 11
print(a != b)

# operatori de comparare: "<", ">", "<=", ">="  - semnul se pune inainte de egal
print(a < b)
print(a > b)
print(a >= b)
print("a <= b ", a <= b)




